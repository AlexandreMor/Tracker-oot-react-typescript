export type Locations = {
  overworld: Array<Location>;
  dungeons: Array<Location>;
  handleItemBox: (
    idArea: number,
    idCheck: number,
    category: "overworld" | "dungeons"
  ) => void;
  closeItemsBox: (
    idArea: number,
    idCheck: number,
    category: "overworld" | "dungeons"
  ) => void;
  handleItemCheck: (
    idArea: number,
    idCheck: number,
    category: "overworld" | "dungeons",
    item: string
  ) => void;
  handleCheckReachable: (
    idArea: number,
    idCheck: number,
    category: "overworld" | "dungeons",
    reachable: "yes" | "out of logic" | "can be seen" | "no"
  ) => void;
  handleDungeonsEntrance: (idArea: number, entrance: string) => void;
  handleVisibility: (
    idArea: number,
    category: "overworld" | "dungeons"
  ) => void;
  handleCheckOpacity: (
    idArea: number,
    idCheck: number,
    category: "overworld" | "dungeons",
  ) => void;
};

